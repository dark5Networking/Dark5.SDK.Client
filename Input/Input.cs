		/// <summary>
		/// Determines whether the specified user input control is enabled.
		/// </summary>
		/// <param name="control">The user input control.</param>
		/// <returns>
		///   <c>true</c> if the specified user input control is enabled; otherwise, <c>false</c>.
		/// </returns>
		public static bool IsControlEnabled(InputControl control)
		{
			return API.IsControlEnabled(0, (int)control);
		}

		/// <summary>
		/// Enables the specified user input control.
		/// </summary>
		/// <param name="control">The user input control to enable.</param>
		public static void EnableControl(InputControl control)
		{
			API.DisableControlAction(0, (int)control, false);
		}

		/// <summary>
		/// Disables the specified user input control.
		/// </summary>
		/// <param name="control">The user input control to disable.</param>
		public static void DisableControl(InputControl control)
		{
			API.DisableControlAction(0, (int)control, true);
		}

		public static bool IsControlModifierPressed(InputModifier modifier)
		{
			if (modifier == InputModifier.Any) return true;

			var bitMask = InputModifierFlagToValue.Where(m => Game.IsControlPressed(0, (Control)m.Value)).Aggregate(InputModifier.None, (current, m) => current | m.Key);

			return bitMask == modifier;
		}

		public static bool IsLastInputMethod(InputMethod method)
		{
			if (method == InputMethod.Any) return true;
			return LastInputMethod == method;
		}

		public static bool IsAnyControlJustPressed(InputMethod method = InputMethod.Any)
		{
			return Enum.GetValues(typeof(InputControl)).Cast<InputControl>().Any(value => IsControlJustPressed(value, InputModifier.None, method));
		}

		public static bool IsControlPressed(InputControl control, InputModifier modifier = InputModifier.None, InputMethod method = InputMethod.Any)
		{
			return API.IsDisabledControlPressed(0, (int)control) && IsControlModifierPressed(modifier) && IsLastInputMethod(method);
		}

		public static bool IsControlJustPressed(InputControl control, InputModifier modifier = InputModifier.None, InputMethod method = InputMethod.Any)
		{
			return API.IsDisabledControlJustPressed(0, (int)control) && IsControlModifierPressed(modifier) && IsLastInputMethod(method);
		}

